import Foundation
import UIKit

// MARK: - Font Size
public extension CGFloat {
    static let nanoFont: CGFloat = 10

    /// CGFloat: 12
    static let tinyFont: CGFloat = 12

    /// CGFloat: 14
    static let smallFont: CGFloat = 14

    /// CGFloat: 16
    static let regularFont: CGFloat = 16

    /// CGFloat: 18
    static let mediumFont: CGFloat = 18

    /// CGFloat: 20
    static let largeFont: CGFloat = 20
}

// MARK: - View Padding
public extension CGFloat {
    /// CGFloat 4
    static let picoPadding: CGFloat = 4
    
    /// CGFloat 6
    static let nanoPadding: CGFloat = 6

    /// CGFloat 8
    static let tinyPadding: CGFloat = 8
    
    /// CGFloat 10
    static let smallPadding: CGFloat = 10
    
    /// CGFloat 16
    static let regularPadding: CGFloat = 16
    
    /// CGFloat 20
    static let mediumPadding: CGFloat = 20
    
    /// CGFloat 24
    static let largePadding: CGFloat = 24

}

// MARK: - View radius
public extension CGFloat {
    /// CGFloat 4
    static let smallRadius: CGFloat = 4
    
    /// CGFloat 8
    static let regularRadius: CGFloat = 8
    
    /// CGFloat 10
    static let mediumRadius: CGFloat = 10
    
    /// CGFloat 12
    static let largeRadius: CGFloat = 12
    
    /// CGFloat 14
    static let extraLargeRadius: CGFloat = 14
    
    /// CGFloat 16
    static let extraLargePlusRadius: CGFloat = 16
}

